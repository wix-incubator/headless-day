import { readFileSync, writeFileSync } from 'node:fs';
import { randomUUID } from 'node:crypto';

const TOKEN = readFileSync('/tmp/wix_token.txt', 'utf8').trim();
const SITE_ID = 'f2b2897c-8f37-4fe4-9816-8da3d89e037e';
const BASE = 'https://www.wixapis.com';
const H = { Authorization: `Bearer ${TOKEN}`, 'wix-site-id': SITE_ID, 'Content-Type': 'application/json' };
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function post(path, body) {
  const res = await fetch(BASE + path, { method: 'POST', headers: H, body: JSON.stringify(body) });
  const text = await res.text();
  let json; try { json = JSON.parse(text); } catch { json = { raw: text }; }
  if (!res.ok) throw new Error(`${res.status} ${path}: ${text.slice(0, 300)}`);
  return json;
}
async function generate(prompt, width, height) {
  const json = await post('/runwareschemaless/v1/request', [{
    taskType: 'imageInference', taskUUID: randomUUID(), outputType: 'URL',
    outputFormat: 'PNG', positivePrompt: prompt, width, height, model: 'google:4@2', numberResults: 1,
  }]);
  const arr = json.data || json.results || (Array.isArray(json) ? json : []);
  const url = arr.find((x) => x && (x.imageURL || x.imageUrl))?.imageURL;
  if (!url) throw new Error('no imageURL: ' + JSON.stringify(json).slice(0, 200));
  return url;
}
async function importMedia(tempUrl, displayName) {
  const json = await post('/site-media/v1/files/import', { url: tempUrl, mimeType: 'image/png', displayName });
  const f = json.file || json;
  if (!f.url) throw new Error('no file.url: ' + JSON.stringify(json).slice(0, 200));
  return f.url;
}

